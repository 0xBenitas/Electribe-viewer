// Décalage entre la valeur SysEx brute (« Oscillator Type », 0~500 dans la doc
// MIDI Korg) et le numéro d'affichage machine (1..409).
// CONFIRMÉ sur hardware (EMX2, 2026-05-23, cf. ADR-002) : interprétation 0-based,
// brut 0 == oscillateur n°1. Donc numéro d'affichage = brut + 1.
export const OSC_RAW_OFFSET = 1;

/** Oscillateur correspondant à une valeur SysEx brute, ou null si hors plage. */
export function oscByRaw(raw: number): Oscillator | null {
  return OSCILLATORS[raw - 1 + OSC_RAW_OFFSET] ?? null;
}

/** Libellé court « Nom » (fallback « OSC <raw> » si inconnu). */
export function oscLabel(raw: number): string {
  return oscByRaw(raw)?.name ?? `OSC ${raw}`;
}
